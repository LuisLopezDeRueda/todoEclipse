package ejemplo.xmlFinal.service;

public class AnimalesXMLException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4377462396561238315L;

	public AnimalesXMLException() {
	}

	public AnimalesXMLException(String message) {
		super(message);
	}

	public AnimalesXMLException(Throwable cause) {
		super(cause);
	}

	public AnimalesXMLException(String message, Throwable cause) {
		super(message, cause);
	}

	public AnimalesXMLException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
