package ceu.dam.ad.ejerciciosTema3.xml.ejercicio12.solucionJackson.services;

public class CatalogoXMLException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4137299625482263651L;

	public CatalogoXMLException() {
	}

	public CatalogoXMLException(String message) {
		super(message);
	}

	public CatalogoXMLException(Throwable cause) {
		super(cause);
	}

	public CatalogoXMLException(String message, Throwable cause) {
		super(message, cause);
	}

	public CatalogoXMLException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
